package com.bridgelabz;

public class SwapTwoNumber {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c;
        System.out.println("Swaping a Two Number");
        c = a;
        a = b;
        b = c;
        System.out.println("After Swaping a = " + a + " After Swaping = " + b);
    }
}
